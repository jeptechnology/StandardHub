#ifndef _STDINT_EX
#define _STDINT_EX

#include <stddef.h>
#include <stdint.h>

// extended definitions of stdint for shorter more compact form of notation based upon stdint
typedef char     char8;
typedef uint8_t  u8;
typedef int8_t   s8;
typedef uint16_t u16;
typedef int16_t  s16;
typedef uint32_t u32;
typedef uint64_t u64;
typedef int32_t  s32;
typedef int64_t  s64;

#endif // _STDINT_EX
